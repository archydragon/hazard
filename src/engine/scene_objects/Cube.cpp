unsigned int Cube::draw(glm::mat4 projection, glm::mat4 view) {
    unsigned int drawCalls = DrawableObject::draw(projection, view);

    glBindVertexArray(vao);
    glDrawArrays(GL_TRIANGLES, 0, 36);
    drawCalls++;
    glBindVertexArray(0);

    return drawCalls;
}
